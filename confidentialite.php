        <h2>1. Objet et champ d’application</h2>
        <p>
            Formatou (ci-après « nous ») attache une grande importance à la protection de la vie privée des utilisateurs 
            (ci-après « vous »). Nous nous engageons à assurer le meilleur niveau de protection de vos données personnelles 
            conformément au Règlement Général sur la Protection des Données (RGPD) et à la loi Informatique et Libertés.
        </p>
        <p>
            Cette politique s’applique à toutes les données collectées via les sites internet de Formatou, dans le cadre 
            de votre navigation, de vos demandes d’informations ou de vos inscriptions à nos formations.
        </p>

        <h2>2. Objectifs et fondements du traitement de vos données</h2>
        <p>Les données collectées servent à :</p>
        <ul>
            <li>Gérer vos demandes d’information, d’aide ou de contact</li>
            <li>Traiter vos inscriptions et paiements aux formations</li>
            <li>Gérer votre compte en ligne et personnaliser votre expérience</li>
            <li>Vous proposer nos actions et formations à proximité</li>
            <li>Publier les résultats de formations</li>
            <li>Respecter nos obligations légales et réglementaires</li>
        </ul>
        <p>
            Les traitements sont fondés sur l’intérêt légitime de Formatou, l’exécution d’un contrat, 
            ou le respect d’une obligation légale.
        </p>

        <h2>3. Catégories de données personnelles collectées</h2>
        <p>Les données collectées varient selon l’objectif du traitement :</p>
        <ul>
            <li><strong>Identification :</strong> nom, prénom, adresse, téléphone, email</li>
            <li><strong>Données de connexion :</strong> identifiants, historique d’accès</li>
            <li><strong>Données de paiement :</strong> mode de paiement, numéro de carte anonymisé, IBAN</li>
            <li><strong>Données liées à la formation :</strong> inscription, résultats, établissement fréquenté</li>
        </ul>
        <p>
            Les champs obligatoires sont signalés par un astérisque rouge (*) dans les formulaires. 
            L’absence de renseignement d’un champ obligatoire peut empêcher le traitement de votre demande.
        </p>

        <h2>4. Destinataires de vos données</h2>
        <p>
            Vos données sont destinées exclusivement aux services internes de Formatou, à nos établissements et 
            à nos prestataires techniques intervenant pour notre compte. Ces derniers sont tenus par des obligations 
            contractuelles de confidentialité et de sécurité.
        </p>
        <p>
            Certaines données peuvent être transférées hors de l’Union Européenne, sous couvert de garanties légales 
            appropriées (clauses contractuelles types ou Privacy Shield).
        </p>

