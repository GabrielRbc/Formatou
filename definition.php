    <div class="content-section">
        <h2>1. Rectification</h2>
        <p>
            Le droit de rectification permet à un utilisateur de demander la correction de données personnelles inexactes ou incomplètes le concernant.
            Ex. : modifier une adresse postale ou corriger une erreur de nom.
        </p>

        <h2>2. Suppression</h2>
        <p>
            Ce droit permet à un utilisateur de demander l’effacement de ses données personnelles lorsque certaines conditions sont remplies, par exemple :

            - la donnée n’est plus nécessaire,
            - la personne retire son consentement,
            - la donnée a été traitée de manière illicite.

            Ce droit n’est cependant pas absolu (certaines obligations légales peuvent empêcher la suppression).
        </p>

        <h2>3. Limitation</h2>
        <p>
            Ce droit permet à un utilisateur d'obtenir la suspension temporaire de l’utilisation de ses données.
            Pendant la limitation, l’organisation conserve les données mais ne les utilise plus, hormis pour le stockage.
            Ex. en cas de contestation de l’exactitude des données le temps de vérification.
        </p>

        <h2>4. Opposition</h2>
        <p>
            Ce droit permet à un utilisateur de s’opposer à tout moment à l’utilisation de ses données pour un traitement donné.
            Dans certains cas, l’opposition nécessite une justification liée à la situation particulière de l’utilisateur.
        </p>

        <h2>5. Portabilité</h2>
        <p>
            Le droit à la portabilité permet à un utilisateur de récupérer ses données personnelles dans un format structuré, couramment utilisé et lisible par machine, et/ou de les transmettre directement à un autre responsable du traitement.
            Ex. : transférer ses données d’un prestataire de service à un autre.
        </p>

